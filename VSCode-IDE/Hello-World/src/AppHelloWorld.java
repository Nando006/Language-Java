public class AppHelloWorld {
    public static void main(String[] args) throws Exception {
        System.out.println("Hello, World!");

        int a = 10;
        int b = 20;

        int sum = a + b;
        System.out.println("Soma de A com B: " + sum);

        int sub = a - b;
        System.out.println("Subtração de A com B: " + sub);

        int mult = a * b;
        System.out.println("Multiplicação de A com B: " + mult);

        int div = a / b;
        System.out.println("Divisão de A com B: " + div);
        
    }
}
